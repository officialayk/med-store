    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
            searchdata= searchfield.getText();
            if(searchdata.trim().length()!=0){
            try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection conn=DriverManager.getConnection("jdbc:mysql://localhost:3306/mymed","root","");
            String sql="select * from customerdata where CID='"+searchdata+"'";
            PreparedStatement pst = conn.prepareStatement(sql);
            rs=pst.executeQuery();
            if(rs.next()){
                cust = rs.getString("CustomerName");
                cont = rs.getString("Contact");
                addr = rs.getString("Address");
                nmfield.setText(cust);
                cntctfield.setText(cont);
                addrsfield.setText(addr);
                
            }
            }
            catch(Exception e){
                JOptionPane.showMessageDialog(null, e);
            }
            }
            else{
                    JOptionPane.showMessageDialog(null, "Enter Customer ID To Search ");
                    }
    }//GEN-LAST:event_jButton1ActionPerformed

