    private void autoId(){
        try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection conn=DriverManager.getConnection("jdbc:mysql://localhost:3306/mymed","root","");
            String sql = "select CID from customerdata order by CID desc limit 1";
            pst=conn.prepareStatement(sql);
            rs=pst.executeQuery();
            if(rs.next()){
                String id = rs.getString("CID");
                int idn = Integer.parseInt(id);
                idn++;
                String uid = Integer.toString(idn);
                cidfield.setText(uid);
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }

    private void myCombo(){
        try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection conn=DriverManager.getConnection("jdbc:mysql://localhost:3306/mymed","root","");
            String sql = "select * from drugdata";
            pst=conn.prepareStatement(sql);
            rs=pst.executeQuery();
            while(rs.next()){
                String print = rs.getString("DrugName");
                drugcombo.addItem(print);     
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }
